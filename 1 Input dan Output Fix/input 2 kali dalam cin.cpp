#include <iostream>

using namespace std;

int main() {
    int x, y;

    // Membaca dua bilangan bulat dalam satu baris
    cout << "Masukkan dua bilangan bulat, dipisahkan dengan spasi: ";
    cin >> x >> y;

    // Menuliskan hasil penjumlahan kedua bilangan ke layar
    cout << "Hasil penjumlahan kedua bilangan adalah " << x + y << endl;

    return 0;
}